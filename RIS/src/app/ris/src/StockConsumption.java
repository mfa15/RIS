    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(!txtQuantity.getText().equalsIgnoreCase(""))
        {
            String quantity ="";
            String itemId = getItemId(cmbStock.getSelectedItem().toString());
            if(radioConsumed.isSelected())
            {
                quantity = "-"+txtQuantity.getText();
            }
            else
            {
                quantity = txtQuantity.getText();
            }
            if(d.insertStock(itemId,quantity))
            {
                JOptionPane.showMessageDialog(this, "Information Saved.");
                clear();
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

