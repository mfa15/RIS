    private void clear() {
        cmbStock.setSelectedIndex(0);
        txtQuantity.setText("");
        radioReceived.setSelected(true);
        
        fillStockTable();
    }

    private void fillStockCmb() {
        lhmItem.clear();
        cmbStock.removeAllItems();
        
        d.getItems(lhmItem);
        if(lhmItem.size()>0)
        {
            fillCmb();
        }
    }

    private void fillCmb() {
        
        for(String key : lhmItem.keySet())
        {
            cmbStock.addItem(lhmItem.get(key));
        }
    }

    private void fillStockTable() {
        clearTable();
        for(String key:lhmItem.keySet())
        {
            lhmStock.clear();
            d.getStockInformation(key,lhmStock);
            if(lhmStock.size()>0)
            {
                for(String id:lhmStock.keySet())
                {
                    fillTable(id,lhmItem.get(key),lhmStock.get(id));
                }
            }
        }
        
        
        
    }
    private void clearTable() {
        this.tableStock.clearSelection();
        
        DefaultTableModel dm = (DefaultTableModel)tableStock.getModel();
        dm.getDataVector().removeAllElements();
        dm.fireTableDataChanged(); 
    }

    private String getItemId(String value) {
        String id="";
        for(String key:lhmItem.keySet())
        {
            if(lhmItem.get(key).equalsIgnoreCase(value))
            {
                id=key;
            }
        }
        return id;
    }

    private void fillTable(String id, String item,String stock) {
        ListSelectionModel lsm = this.tableStock.getSelectionModel();
        lsm.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        //------------- Information Filled in table
        DefaultTableModel model = (DefaultTableModel) tableStock.getModel();    
        
            Object arr[] = {id,item,stock};
                model.addRow(arr);
            
        this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
    }
