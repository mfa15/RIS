    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        if(!txtTitle.getText().equals("") && !txtLine2.getText().equals("") && !txtQualification.getText().equals(""))
        {
            if(d.insertDoctorInfo(txtTitle.getText(),txtLine2.getText(),txtQualification.getText(),txtSid.getText(),regObj.updateFlag)
                    && d.insertFinalSignAuthority(txtSid.getText(),chkFinalSign.isSelected()?"Y":"N",ebo.getLocation(),flag,"0"))
            {
                JOptionPane.showMessageDialog(this, "Information Saved Correctly.");
                exit();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "All values are Mandatory. Please fill complete Information");
            return;
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void formFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_formFocusGained
        txtSid.setText(regObj.txtID.getText());
        txtSname.setText(regObj.txtName.getText());
        
        if(regObj.updateFlag)
        {
            dam d = new dam();
            String result="";
            String finalSign="";
            result = d.getDoctorInfo(txtSid.getText());
            finalSign = d.getFinalSignInfo(txtSid.getText());
            if(result!="")
            {
                String arr[] = result.split("###");
                if(arr.length==3)
                {
                    txtTitle.setText(arr[0]);
                    txtLine2.setText(arr[1]);
                    txtQualification.setText(arr[2]);
                }
            }
            else
            {
                regObj.updateFlag=false;
            }
            
            if(!finalSign.equalsIgnoreCase(""))
            {
                chkFinalSign.setSelected(finalSign.equalsIgnoreCase("Y"));
                flag=true;
            }
            else
            {
                flag=false;
            }
            
        }
    }//GEN-LAST:event_formFocusGained


